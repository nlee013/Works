package com.day4;

//다중 for문

public class Test19 {

	public static void main(String[] args) {

		int i, j;
		
		for(i = 2; i <= 9; i++) {
			
			System.out.println( i + "unit");
			
			System.out.println("------------------");

			for(j = 1; j <=9; j++) {
				
				//System.out.println(i + "x" + j + "=" + (i*j));
				System.out.printf("%d*%d=%d\n", i, j, (i*j));
			}
			System.out.println(); //for문이 실행되고 위로 다시 올라가기 때문에 여기에 해야 처음부터 띄어쓰기를 안하고 가능.
		}
		
	}

}
